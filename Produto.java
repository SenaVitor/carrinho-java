public class Produto {

  String nome;
  String descricao;
  String fabricante;
  String vendedor;
  float preco;

  Produto(String nome, String descricao, String fabricante, String vendedor, float preco){
    this.nome = nome;
    this.descricao = descricao;
    this.fabricante = fabricante;
    this.vendedor = vendedor;
    this.preco = preco;
  }
}